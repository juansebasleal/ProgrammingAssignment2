## Functions to cache solve operation (inverse) on a matrix
## so that we can speed up data analysis

## This function takes a matrix x and creates a list of fuctions
## useful to cache data and speed up operations over matrix x
makeCacheMatrix <- function(x = matrix()) {
  s <- NULL
  set <- function(y) {
    x <<- y
    s <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) s <<- solve
  getsolve <- function() s
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}


## This function calculates the inverse of a matrix if
## it hasn't been calculated before
## Returns a matrix that is the inverse of 'x'
cacheSolve <- function(x, ...) {
  s <- x$getsolve()
  if(!is.null(s)) {
    message("getting cached data")
    return(s)
  }
  data <- x$get()
  s <- solve(data, ...)
  x$setsolve(s)
  s
}
